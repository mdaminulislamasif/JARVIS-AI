    def analyze_current_code(self):
        """Analyze JARVIS's current codebase"""
        print("\n🔍 Analyzing current JARVIS code...")
        
        analysis_results = []
        
        # Find all Python files
        for root, dirs, files in os.walk('.'):
            # Skip certain directories
            if any(skip in root for skip in ['.git', '__pycache__', 'venv', 'node_modules']):
                continue
            
            for file in files:
                if file.endswith('.py') and file.startswith('jarvis_'):
                    filepath = os.path.join(root, file)
                    analysis = self._analyze_file(filepath)
                    if analysis:
                        analysis_results.append(analysis)
        
        print(f"✅ Analyzed {len(analysis_results)} JARVIS files")
        return analysis_results
    
    def _analyze_file(self, filepath):
        """Analyze a single Python file"""
        try:
            with open(filepath, 'r', encoding='utf-8') as f:
                code = f.read()
            
            # Parse AST
            tree = ast.parse(code)
            
            # Count elements
            functions = [node for node in ast.walk(tree) if isinstance(node, ast.FunctionDef)]
            classes = [node for node in ast.walk(tree) if isinstance(node, ast.ClassDef)]
            lines = len(code.split('\n'))
            
            # Calculate complexity (simple metric)
            complexity = len(functions) + len(classes) * 2
            
            # Store analysis
            cursor = self.conn.cursor()
            cursor.execute('''
                INSERT INTO code_analysis 
                (file_path, lines_of_code, functions_count, classes_count, complexity_score)
                VALUES (?, ?, ?, ?, ?)
            ''', (filepath, lines, len(functions), len(classes), complexity))
            self.conn.commit()
            
            return {
                'file': filepath,
                'lines': lines,
                'functions': len(functions),
                'classes': len(classes),
                'complexity': complexity
            }
        
        except Exception as e:
            print(f"⚠️ Error analyzing {filepath}: {e}")
            return None
    
